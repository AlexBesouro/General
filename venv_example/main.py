"Commands i used"

"python3 -m -venv <name> (almost always venv)" # command to create a virtual environment 
# python3 - its a launcher for python
# -3 - specifies to use python 3
# -m - tells python to run a module as a script
# -venv - the module to create virtual environments 
# <name> - the name of the virtual environment folder
# rm -rf venv # command to delete a virtual environment folder

"source <name>/bin/activate" # command to activate the virtual environment
# source - a shell built-in command to run commands from a file in the current shell
# <name>/bin/activate - the path to the activate script inside the virtual environment folder
# deactivate # command to deactivate the virtual environment

